# ? says that the group matches 0-1 times
# * says the group matches 0 or more times
# + says the group matches 1+ times
# curly braces can match a specific number of times
# curly braces with 2 numbers matches min max # of times
# leaving of first or 2nd says there is no min or max
# greedy matching matches longest string possible, non greedy shortest
# putting a ? after the braces makes it do nongreedy matching
